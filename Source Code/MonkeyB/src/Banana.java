
public class Banana {
	private  int banana_x; 
	private  int banana_y;
	
	public Banana()
	{
		this.banana_x=0;
		this.banana_y=0;
	}
	
	public int getBananaX()
	{
		return banana_x;
	}
	
	public void setBananaX(int x)
	{
		banana_x=x;
	}
	
	public int getBananaY()
	{
		return banana_y;
	}
	
	public void setBananaY(int y)
	{
		banana_y=y;
	}
}
